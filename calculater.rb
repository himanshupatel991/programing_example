def sum_of_two_number(number1, number2)
  puts number1 + number2
end

def substraction_of_two_number(number3, number4)
  puts number3 - number4
end

def multiplication_of_two_number(z, x)
  puts z * x
end

def divide_of_two_number(a, s)
  puts a / s
end

def modulo_of_two_numbers(h, i)
  puts h % i
end

sum_of_two_number(9, 5)
substraction_of_two_number(83, 45)
multiplication_of_two_number(45, 5)
divide_of_two_number(65, 5)
modulo_of_two_numbers(7, 4)
